Dans ce cours, nous allons découvrir la puissance des API (Application Programming Interfaces) pour créer des applications plus riches. Les API REST en particulier sont de plus en plus utilisées car elles fonctionnent de la même manière que le reste du web : vous ne serez pas perdus !

En utilisant l'API d'une autre application, vous aurez accès à des données utiles qui vous feront gagner du temps et de l'énergie. Par exemple, si vous utilisez l'API d'un service de météo, vous pourrez intégrer dans votre propre application leurs données de température, de pluie, etc ! ☀☔

Ça vous tente ? Alors venez apprendre à utiliser les API REST d'Instagram et de Gmail, ou encore l'API de Watson, une intelligence artificielle qui va transcrire votre voix en texte.

Suivez ce cours pour commencer à profiter de toutes les API qui existent !

Objectifs pédagogiques : 
À la fin de ce cours, vous saurez...
ce qu'est une API et pourquoi vous devriez en utiliser ;
les critères qui définissent une API REST ;
la différence entre les API SOAP et REST ;
comment est construite une API REST ;
comment envoyer des requêtes à une API externe ;
les bases pour construire sa propre API.
